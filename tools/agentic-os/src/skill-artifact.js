'use strict';

const fs   = require('node:fs');
const path = require('node:path');

// generateArtifact — builds a markdown skill artifact string from a session record.
// Purpose: Converts a completed (or in-progress) session into a structured markdown
//   document that can be stored in a Claude Code skills directory and referenced in
//   future orchestration runs. The format mirrors the Claude Code skill convention:
//   a top-level heading, metadata block, then sections for summary, phase outcomes,
//   tool usage, and per-phase agent lists.
// Params:
//   session (Object) — session record as stored by session-manager.js. Expected shape:
//     id             (string)         — unique session identifier
//     startTime      (string)         — ISO 8601 start timestamp (session-manager field name)
//     endTime        (string|null)    — ISO 8601 end timestamp, or null if still running
//     agentName      (string)         — human-readable name of the agent
//     tasksCompleted (number)         — tasks completed when session ended
//     tasksPlanned   (number)         — tasks planned at session start
//     skillsUsed     (Array<string>)  — skill identifiers used during the session
//     outcome        (string)         — 'in-progress'|'success'|'partial'|'failed'
// Returns: string — full markdown content for the skill artifact file
// Notes: Defensive against missing or malformed fields — each section falls back
//   gracefully to placeholder text so the output is always valid markdown.
//   The caller (writeArtifact) is responsible for persisting the string.
function generateArtifact(session) {
  // Map session-manager field names to display values with safe fallbacks.
  // session-manager stores startTime/endTime (not startedAt/endedAt).
  const id        = session.id        || 'unknown';
  const startedAt = session.startTime || 'unknown';
  const endedAt   = session.endTime   ?? null;
  const outcome   = session.outcome   || 'unknown';
  const agentName = session.agentName || 'unknown';
  const skillsUsed     = Array.isArray(session.skillsUsed) ? session.skillsUsed : [];
  const tasksCompleted = typeof session.tasksCompleted === 'number' ? session.tasksCompleted : 0;
  const tasksPlanned   = typeof session.tasksPlanned   === 'number' ? session.tasksPlanned   : 0;

  // ---- Header block --------------------------------------------------------
  const lines = [];
  lines.push(`# Skill Artifact: ${id}`);
  lines.push('');
  lines.push(`**Session**: \`${id}\``);
  lines.push(`**Agent**: ${agentName}`);
  lines.push(`**Started**: ${startedAt}`);
  lines.push(`**Ended**: ${endedAt !== null ? endedAt : 'In Progress'}`);
  lines.push(`**Outcome**: ${outcome}`);
  lines.push(`**Tasks**: ${tasksCompleted} / ${tasksPlanned} completed`);
  lines.push('');

  // ---- Summary section -----------------------------------------------------
  // Skills used during the session serve as the summary — more useful than a
  // free-text field that session-manager doesn't record.
  lines.push('## Summary');
  if (skillsUsed.length > 0) {
    lines.push('**Skills used:**');
    for (const skill of skillsUsed) {
      lines.push(`- ${skill}`);
    }
  } else {
    lines.push('_No skills recorded._');
  }
  lines.push('');

  // ---- Footer --------------------------------------------------------------
  lines.push('---');
  lines.push('_Generated by Agentic OS_');

  return lines.join('\n');
}

// writeArtifact — generates and writes a skill artifact markdown file to disk.
// Purpose: Wraps generateArtifact with file I/O so callers do not have to manage
//   directory creation, filename generation, or write errors. The filename embeds
//   a Date.now() timestamp so successive calls for the same session id produce
//   distinct files rather than overwriting each other.
// Params:
//   session   (Object) — session record passed directly to generateArtifact (see above)
//   outputDir (string) — absolute path to the directory where the file will be written.
//     Created recursively if it does not already exist.
// Returns: string — the absolute path of the file that was written
// Notes: fs.mkdirSync with { recursive: true } is used so the function works on a
//   clean system where no parent directories exist yet. writeFileSync is synchronous
//   (not async) so the returned path is guaranteed to exist when the function returns —
//   callers can immediately open the file for reading or pass the path to another tool.
//   On any error, a console.error is emitted and the error is re-thrown so the caller
//   (e.g. the session:end IPC handler) can choose whether to swallow or surface it.
function writeArtifact(session, outputDir) {
  try {
    // Ensure the output directory exists before attempting to write.
    fs.mkdirSync(outputDir, { recursive: true });

    // Filename includes session id and a millisecond timestamp for uniqueness.
    // Use a local safe fallback so undefined id never produces 'skill-undefined-*.md'.
    const safeId   = (session && session.id) ? session.id : 'unknown';
    const filename = `skill-${safeId}-${Date.now()}.md`;
    const filePath = path.join(outputDir, filename);

    const content = generateArtifact(session);
    fs.writeFileSync(filePath, content, 'utf8');

    console.log(`[skill-artifact] Artifact written to ${filePath}`);
    return filePath;
  } catch (err) {
    console.error('skill-artifact: write failed', err.message);
    throw err;
  }
}

// listArtifacts — returns all skill artifact markdown files in an output directory,
//   sorted newest-first.
// Purpose: Allows the renderer or IPC handlers to show a list of previously generated
//   artifacts without needing to know the exact filenames. Sorting by filename
//   descending works because filenames include a millisecond timestamp component
//   (skill-<id>-<timestamp>.md), so lexicographic descending order is chronological
//   descending order.
// Params:
//   outputDir (string) — absolute path to the directory to scan
// Returns: Array<string> — absolute file paths of all skill-*.md files in outputDir,
//   sorted newest-first (lexicographic descending on filename). Returns [] if
//   outputDir does not exist rather than throwing ENOENT.
// Notes: Uses readdirSync — appropriate here because artifact directories are small
//   (dozens of files at most) and listing is a fast, infrequent operation. If deeper
//   nesting or large directories are needed in the future, a recursive walk or a
//   database-backed index would be a better fit.
function listArtifacts(outputDir) {
  // Return an empty array rather than throwing if the directory has never been created.
  // This is expected on a fresh install where no sessions have been ended yet.
  if (!fs.existsSync(outputDir)) {
    return [];
  }

  try {
    const entries = fs.readdirSync(outputDir);

    // Filter to only skill-*.md files, sort descending so newest appears first.
    const artifacts = entries
      .filter(name => name.startsWith('skill-') && name.endsWith('.md'))
      .sort()
      .reverse()
      .map(name => path.join(outputDir, name));

    return artifacts;
  } catch (err) {
    // If readdirSync fails for any reason other than ENOENT (e.g. permission denied),
    // log the error and return an empty array — a missing artifact list is a
    // non-fatal degradation and should not crash the caller.
    console.error('[skill-artifact] listArtifacts: failed to read directory:', err.message);
    return [];
  }
}

// Export the public API. generateArtifact is exported separately so callers can
// produce the markdown string without writing it to disk (useful for preview/testing).
module.exports = { generateArtifact, writeArtifact, listArtifacts };
